  HAL_Delay(10);
  _RefreshWatchdogs();

  DEBUG_PRINTF("Calibrating ADC...\n");
  if (ADC_Calibrate() != HAL_OK) {
    Error_Handler();
  }

  _RefreshWatchdogs();

  DEBUG_PRINTF("ADC cal complete, starting monitoring\n");
  if (ADC_StartMonitoring() != HAL_OK) {
    Error_Handler();
  }

  HAL_Delay(10);
  _RefreshWatchdogs();

  DEBUG_PRINTF("Initializing safety system...\n");
